                            $(document).ready(function(){
                                // $('.thanks-content').hide();
                                $("#contactForm").submit(function () {
                                var email = $("#email").val();
                                var fn = $("#fn").val();
                                var message = $("#message").val();
                                var products = $( "#products option:selected" ).val();
                                if(products == '' && fn == '' && message == '' && products == ''){
                                    $("#email").addClass('errorClass');
                                    $("#fn").addClass('errorClass');
                                    $("#message").addClass('errorClass');
                                    $("#products").addClass('errorClass');
                                    // alert('if'); 
                                    return false;
                                } else if(email == ''){
                                    $("#email").addClass('errorClass');
                                    return false;
                                } else if(fn == ''){
                                    $("#fn").addClass('errorClass');
                                    return false;
                                } else if(message == ''){
                                    $("#message").addClass('errorClass');
                                    return false;
                                }else if(products == ''){
                                    $("#products").addClass('errorClass');
                                    return false;
                                }else{
                                    $("#email").removeClass('errorClass');
                                    $("#fn").removeClass('errorClass');
                                    $("#message").removeClass('errorClass');
                                    $("#products").removeClass('errorClass');
                                    $("#contactForm .btn-submit").attr('disabled', 'disabled');
                                }                               
                                //alert('else'); return false;
                                //alert(email);
                                $.ajax({
                                    url: "handler.php",
                                    method: "post",
                                    data: {email: email, fn: fn, message: message, products: products},
                                    dataType: "json",
                                    success: function(data) {
                                    if(data["flag"] == "success"){
                                        window.location = 'https://nsdemo.in/bhnsurgical/thank-you.php';
                                        }else if(data.status == 'fail'){
                                            alert("Email Empty!");
                                        }else if(data.status == 'error'){
                                            alert("Error on query!");
                                        }
                                    },
                                  });
                                  return false;
                                });
                             });
